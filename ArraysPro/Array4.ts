let animals : string[] = ["Lion", "Cow", "Cat", "Dog", "Elephant", "Tiger", "Fox"];


animals.unshift("Rhyno")  //it stores element at the start part
console.log(animals);

animals.push("Hyppo");   //it stores element at the end part
console.log(animals);

animals.shift();    //it removes first element
console.log(animals);

animals.pop();   //it removes element at the end part
console.log(animals);
